package chap3;
public class IfElseIfElse{
	public static void main(String[] args) {
		int score = 72;
		
		if(score>=90) {
			System.out.println("A입니다.");
		} else if(score>=80) {
			System.out.println("B입니다.");
		} else if(score>=70) {
			System.out.println("C입니다.");
		} else {
			System.out.println("D입니다.");
		}
	}
}